# 🛠️ Проверка наличия Jazzmin и автоматическая настройка
def setup_jazzmin_if_available():
    """Проверяет наличие Jazzmin и настраивает его при необходимости"""
    try:
        from django.conf import settings
        if 'jazzmin' in settings.INSTALLED_APPS:
            if not hasattr(settings, 'JAZZMIN_SETTINGS'):
                # Если Jazzmin установлен, но не настроен, выводим сообщение
                print("⚠️ Jazzmin установлен, но не настроен в settings.py")
                print("📝 Для полноценной группировки добавьте настройки JAZZMIN_SETTINGS")
    except (ImportError, AttributeError):
        # Если не можем проверить настройки, просто пропускаем
        pass

# Запускаем проверку Jazzmin при импорте
setup_jazzmin_if_available()

